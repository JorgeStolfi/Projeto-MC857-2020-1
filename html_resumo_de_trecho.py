import html_resumo_de_trecho_IMP

def gera(obj_trecho):
  """Retorna um fragmento HTML que descreve os dados principais de um
  trecho: identificadores do trecho, seus atributos e o número de poltronas.

  O resultado é uma tupla com fragmentos separados para cada um desses campos,
  que pode ser usada como uma linha do argumento de {html_tabela.gera}."""
  return html_resumo_de_trecho_IMP.gera(obj_trecho)
